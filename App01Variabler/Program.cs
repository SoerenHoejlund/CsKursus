using System;

namespace App01Variabler
{
    class Program
    {
        static void Main(string[] args)
        {
            int a = 10;
            a++;
            a--;
            a += 20;
            Console.WriteLine(a); 

            double b = 12.5;
            b++;
            b--;
            b *= 2;
            Console.WriteLine(b);

        }
    }
}
